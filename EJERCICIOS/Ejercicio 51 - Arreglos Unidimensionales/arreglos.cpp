#include <iostream>

using namespace std;

int main(int argc, char const *argv[])
{
    int numeros[5]; //Decalaración del arreglo

    // Asignación de valores a un arreglo unidimensional
    numeros [0] = 4;
    numeros [1] = 5;
    numeros [2] = 7;
    numeros [3] = 8;
    numeros [4] = 3;

    // Lectura de los valores de un arreglo unidimensional
    cout << numeros [0] << endl;
    cout << numeros [1] << endl;
    cout << numeros [2] << endl;
    cout << numeros [3] << endl;
    cout << numeros [4] << endl;

    return 0;
}
